org 100h
MOV CL, 1

LOOP1:
MOV CH, 4
SUB CH, CL

SPACE:
CMP CH, 0 
JE STAR
MOV AH, 2 
MOV DL, ' ' 
INT 21H
DEC CH 
JMP SPACE 

STAR:
MOV CH, 1 

LOOP2:
MOV AH, 2 
MOV DL, '*' 
INT 21H   

INC CH   

MOV AL, CL 
ADD AL, AL 
DEC AL
CMP CH, AL 
JLE LOOP2  

MOV AH, 2 
MOV DL, 10 
INT 21H
MOV DL, 13 
INT 21H

INC CL 
CMP CL, 4 
JLE LOOP1
RET